# 1. __init__ and Object Initialization
# The __init__ method is the constructor in Python. It is called automatically when a new object of the class is created. 
# Its primary role is to initialize the instance attributes (variables specific to that object).

# 2. Instance and self
# Every method in a class must have self as its first parameter (unless it's a @staticmethod or @classmethod). 
# This allows the method to access or modify the attributes of the specific instance calling it.
